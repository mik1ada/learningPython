#Falsy values in Python - wartości uznawane za fałsz przy konwersji na typ bool
print(bool(False))
print(bool(0))
print(bool(0.0))
print(bool(()))
print(bool([]))
print(bool({}))
print(bool(""))
print(bool(None)) 

#Truthy values in Python - wartości uznawane za prawdę przy konwersji na typ bool
print(bool(True))
print(bool(42))
print(bool(-3.14))
print(bool((1, 2)))
print(bool([0, ""]))
print(bool("z"))
print(bool({0}))



